import SwiftUI

enum AppFont {
    static func spicyRice(size: CGFloat) -> Font {
        Font.custom("SpicyRice-Regular", size: size)
    }
    static func agdasima(size: CGFloat) -> Font {
        Font.custom("Agdasima-Regular", size: size)
    }
}
